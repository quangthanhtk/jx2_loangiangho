			}
			Say(format("Chän vÞ trÝ phßng cô:", GetItemName(nItem)), getn(tSay), tSay);
			return 0;
		end
		local tEquip = {ZHANKUANG_BOX.tHead, ZHANKUANG_BOX.tCloth, ZHANKUANG_BOX.tShoe};
		local tEquipSub = tEquip[nType];
		local nRoute = GetPlayerRoute();
		local nBody = GetBody();
		if not tEquipSub[nRoute] then return 0 end
		t = tEquipSub[nRoute][nBody];
	end
	if not t then return 0; end
	if DelItemByIndex(nItem, 1) ~= 1 then
		return 0;
	end
	AddItem(unpack(t[1]));
	Msg2Player(format("§· nhËn ®­îc %s*%d", t[2], 1));
end