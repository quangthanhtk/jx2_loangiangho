function OpenZhankuangBox(nItem, nType)
	if gf_Judge_Room_Weight(1, 215, " ") ~= 1 then
		return 0;
	end
	local nP = tonumber(GetItemParticular(nItem));
	local t = nil;
	if nP == 31003 then
		local tEquip = ZHANKUANG_BOX.tWeapon;
		local nRoute = GetPlayerRoute();
		local nBody = GetBody();
		if not tEquip[nRoute] then return 0 end
		t = tEquip[nRoute][nBody];
	elseif nP == 31002 then
		if not nType then
			local tSay = {
				format("§Çu/#OpenZhankuangBox(%d, 1)", nItem),
				format("Th­îng y/#OpenZhankuangBox(%d, 2)", nItem),
				format("H¹ y/#OpenZhankuangBox(%d, 3)", nItem),
