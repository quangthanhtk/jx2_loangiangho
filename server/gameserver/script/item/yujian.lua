function OnEquip(nItemIndex)
  local nG, nD, nP = GetItemInfoByIndex(nItemIndex);
  local tInfo = g_tInfo[nP];

  if not tInfo then
    return 0;
  end

  if nG ~= tInfo.tItem[1] or nD ~= tInfo.tItem[2] or nP ~= tInfo.tItem[3] then
    return 0;
  end

  if tInfo.nEffectID then

    --LongDaiKa: fix Èn hiÖu øng cña ngùa khi vµo chiÕn tr­êng
    local tbMapNotEffect = {
      [606] = "Thiªn M«n trËn néi",
      [969] = "Hoa S¬n",
      [881] = "ChiÕn tr­êng Th«n trang-Nh¹n M«n Quan",
      [882] = "ChiÕn tr­êng Th¶o cèc-Nh¹n M«n Quan",
      [883] = "ChiÕn tr­êng ph¸o ®µi-Nh¹n M«n Quan",
      [884] = "ChiÕn tr­êng chÝnh-Nh¹n M«n Quan",
    };

    if WTask:GetBit(WTASKID_PLAYERSTATE_BIT, WTASKID_PLAYERSTATE_BIT_DISABLE_EFFECT) == 1 then
      -- trong nµy sÏ kh«ng cã hiÖu øng
      SetHorseEffect(0);
    else
      -- gèc sÏ hiÖn hiÖu øng
      SetHorseEffect(tInfo.nEffectID);
    end
  end

  if tInfo.tLimitSkill then
    for _, nSkillID in tInfo.tLimitSkill do
      LimitSkill(nSkillID);
    end
  end

  if tInfo.bLimitInteractive and 1 == tInfo.bLimitInteractive then
    CleanInteractive();
    InteractiveEnable(0);
  end

  return 1;
end

function OnUnEquip(nItemIndex)
  SetHorseEffect(0);

  local nG, nD, nP = GetItemInfoByIndex(nItemIndex);
  local tInfo = g_tInfo[nP];

  if not tInfo then
    return 0;
  end

  if nG ~= tInfo.tItem[1] or nD ~= tInfo.tItem[2] or nP ~= tInfo.tItem[3] then
    return 0;
  end

  if tInfo.tLimitSkill then
    for _, nSkillID in tInfo.tLimitSkill do
      UnLimitSkill(nSkillID);
    end
  end

  if tInfo.bLimitInteractive and 1 == tInfo.bLimitInteractive then
    InteractiveEnable(1);
  end

  return 0;
end

